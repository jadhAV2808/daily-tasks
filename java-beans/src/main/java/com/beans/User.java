package com.beans;

public class User {
	
	String fname;
	String lname;
	
	public User() {
		fname="Kalyani";
		lname="Jadhav";
	}

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}

	public String getLname() {
		return lname;
	}

	public void setLname(String lname) {
		this.lname = lname;
	}
	
	

}
